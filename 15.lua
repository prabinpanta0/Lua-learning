--[[
    Escape Sequence	Use
    \a	Bell
    \b	Backspace
    \f	Formfeed
    \n	New line
    \r	Carriage return
    \t	Tab
    \v	Vertical tab
    \\	Backslash
    \"	Double quotes
    \'	Single quotes
    \[	Left square bracket
    \]	Right square bracket
--]]

-- new line
print("Escape sequence in Lua: \n new line")

-- bell
print("This should trigger a bell sound? \a\n")

-- backspace
print("Example of backspac1\be\n")

-- formfeed
print("before formfeed\fafter formfeed\n")

-- carriage return
print("123456\rCC\n")

-- tab
print("Sentence \t And \t Line\n")

-- Vertical Tab
print("Vertical\vtab\n")

-- blackslash
print("A blackslash \\\n example\n")

-- Double Quotes
print("I said, \"I am bored\"\n")

-- single quotes
print("It\'s boring\n")

-- Left and right square bracket
print("\[It's a secret\]\n")
