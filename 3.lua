-- operators
a = 5
b = 10

-- Arithmetic Operator
print ("a + b = ", a + b )
print ("a - b = ", a - b )
print ("a * b = ", a * b )
print ("b / a = ", b / a )
print ("a % b = ", a % b )
print ("a ^ 2 = ", a ^ 2 )

-- Relation Operator
print ("a == b : ", a == b )
print ("a ~= b : ", a ~= b )
print ("a < b : ", a < b )
print ("a > b : ", a > b )
print ("a >= b : ", a >= b )
print ("a <= b : ", a <= b )

-- Logical Operator
print ("a and b", a and b )
print ("a or b", a or b )
print ("not (a and b)", not (a and b) )

-- Misc Operator
a = "Hello"
b = "World"

print("a..b", a..b )
print("#a", #a)

